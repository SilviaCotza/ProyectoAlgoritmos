    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        Archivo archivo = new Archivo();
        archivo.crearArchivo();
        
       
        
        if (cajaTextoNombreListaTareas.getText().equals("") || cajaTextoNombreTablero.getText().equals("") || cajaTextoNombreTarea.getText().equals("")||cajaTextoDescripcionTarea.getText().equals("")||cajaTextoListaActividades.getText().equals("")||cajaTextoActividades.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor rellene los campos macardos con * ");

        } else {
            
            String nombreTablero = cajaTextoNombreTablero.getText();
            String nombreListaTareas = cajaTextoNombreListaTareas.getText();
            String nombreTarea = cajaTextoNombreTarea.getText();
            String descripcionTarea = cajaTextoDescripcionTarea.getText();
            String comentarios = cajaTextoComentarios.getText();
            String listaActividades = cajaTextoListaActividades.getText();
            String actividades = cajaTextoActividades.getText();

            Tablero tablero = new Tablero(nombreTablero, nombreListaTareas, nombreTarea,descripcionTarea,comentarios, listaActividades,actividades);
            archivo.escribirEnArchivo(tablero);

            JOptionPane.showMessageDialog(null, "Registro guardado");
        }

    }//GEN-LAST:event_botonGuardarActionPerformed

